import numpy as np

def hat(k): # Returns 3 x 3 cross product matrix for 3 x 1 vector
    khat=np.zeros((3,3))
    khat[0,1]=-k[2]
    khat[0,2]=k[1]
    khat[1,0]=k[2]
    khat[1,2]=-k[0]
    khat[2,0]=-k[1]
    khat[2,1]=k[0]
    return khat

def q2R(q): # Converts quaternion into a 3 x 3 rotation matrix according to the Euler-Rodrigues formula  
    I = np.identity(3)
    qhat = hat(q[1:4])
    qhat2 = qhat.dot(qhat)
    return I + 2*q[0]*qhat + 2*qhat2